    private void checkoutAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutAddButtonActionPerformed
        // TODO add your handling code here:
        errorLabelCheckout.setText("");
        NumberFormat formatter = NumberFormat.getCurrencyInstance();
       
        if (!isInteger(productIDField.getText()) || !isInteger(quantityField.getText())) errorLabelCheckout.setText("*Fields must be Integers");
        else if (Integer.parseInt(quantityField.getText()) < 1) errorLabelCheckout.setText("*Quantity must be greater than 0");
        else {
            int productID = Integer.parseInt(productIDField.getText());
            int quantity = Integer.parseInt(quantityField.getText());
            CheckoutItem temp = MarketPlaceDriver.mainSys.addItemToCheckout(productID, quantity);
            if (temp.getID() != 0) {
                DefaultListModel nameModel = (DefaultListModel) nameList.getModel();
                DefaultListModel quanModel = (DefaultListModel) quantityList.getModel();
                DefaultListModel totalModel = (DefaultListModel) priceList.getModel();
                
                nameModel.addElement(temp.getName());
                quanModel.addElement(temp.getQuantity());
                totalModel.addElement(temp.getPrice());
                
                totalLabel.setText(formatter.format(MarketPlaceDriver.mainSys.getCurrentTotal()));
                subtotalLabel.setText(formatter.format(MarketPlaceDriver.mainSys.getCurrentSubTotal()));
            }
            else errorLabelCheckout.setText("*Product not found");
        }
        
    }//GEN-LAST:event_checkoutAddButtonActionPerformed

