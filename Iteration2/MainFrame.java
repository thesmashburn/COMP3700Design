    private void updateSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateSearchButtonActionPerformed
        // TODO add your handling code here:
        updateErrorLabel.setText("");
        if (isInteger(updateSearchField.getText())) {
            int productID = Integer.parseInt(updateSearchField.getText());
            Product temp = MarketPlaceDriver.mainSys.findProductUpdate(productID);
            if (temp.getID() != 0) {
                updateNameField.setText(temp.getName());
                updatePriceField.setText(temp.getPrice() + "");
                updateVendorField.setText(temp.getVendor());
                updateTaxField.setText(temp.getTax() + "");
                updateWeightField.setText(temp.getWeight() + "");
                changeInputsUpdate(true);
            }
            else {
                updateErrorLabel.setText("*Product not found");
                changeInputsUpdate(false);
            }
        }
        else {
            updateErrorLabel.setText("*Invalid ID format");
            changeInputsUpdate(false);
        }
        
    }//GEN-LAST:event_updateSearchButtonActionPerformed

    private void submitButtonUpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonUpdActionPerformed
        // TODO add your handling code here:
        Boolean priceBool = isDouble(updatePriceField.getText());
        Boolean taxBool = isDouble(updateTaxField.getText());
        Boolean weightBool = isDouble(updateWeightField.getText());
        Boolean nameBool = correctString(updateNameField.getText());
        Boolean vendorBool = correctString(updateVendorField.getText());
        if (!priceBool || !taxBool || !weightBool || !nameBool || !vendorBool) {
            updateErrorLabel.setText("*Incorrect format for given field(s)");
        }
        else {
            double price = Double.parseDouble(updatePriceField.getText());
            double tax = Double.parseDouble(updateTaxField.getText());
            double weight = Double.parseDouble(updateWeightField.getText());
            String name = updateNameField.getText();
            String vendor = updateVendorField.getText();
            
            if (MarketPlaceDriver.mainSys.pushProductUpdate(name, price, vendor, tax, weight)) {
                CardLayout card = (CardLayout)mainPanel.getLayout();
                card.show(mainPanel, "card3");
                homeNoticeLabel.setText("Product succesfully updated!");
            }
        }
        
    }//GEN-LAST:event_submitButtonUpdActionPerformed

